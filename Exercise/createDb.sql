CREATE TABLE CONTO_CORRENTE (
	NUMERO NUMBER PRIMARY KEY,
	IBAN VARCHAR(30) NOT NULL,
	SALDO NUMBER NOT NULL,
	VALUTA VARCHAR(3) NOT NULL
);

CREATE TABLE CLIENTE_CONTO (
	CIF NUMBER PRIMARY KEY,
	CONTO NUMBER,
	FOREIGN KEY (CONTO) REFERENCES CONTO_CORRENTE(NUMERO)
);

CREATE TABLE BONIFICO (
	ID VARCHAR(50) PRIMARY KEY,
	CONTO_RICHIEDENTE NUMBER NOT NULL,
	IMPORTO NUMBER NOT NULL,
	VALUTA VARCHAR(3) NOT NULL,
	DATA_ESECUZIONE DATE NOT NULL,
	NOMINATIVO_BENEFICIARIO VARCHAR(40) NOT NULL,
	IBAN_BENEFICIARIO VARCHAR(30) NOT NULL,
	CRO VARCHAR(50),
	CAUSALE VARCHAR(100) NOT NULL,
	STATUS CHAR(2) NOT NULL,
	FOREIGN KEY (CONTO_RICHIEDENTE) REFERENCES CONTO_CORRENTE(ID)
);

